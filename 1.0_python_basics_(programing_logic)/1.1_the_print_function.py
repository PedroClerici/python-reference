"""
The Print Function:
"""

# Variables examples:
age = 18
year = 2022

# Printing variable to the console:
print(age, year)

# Changing the print separator:
print('Pedro', 'Clerici', sep=' ')

# Changing the default end of the print funciton (\n);
print('Testing1', end='-')
print('Testing2')

# Also the "*" symbol can be used as a repetition operator:
print('Python is fun!' * 10)